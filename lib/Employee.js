class Employee {
    constructor(name, id , email) {
        this.name = name;
        this.id = id; 
        this.email = email;
    }

    inputName () {
        return name;
    }

    inputId () { 
        return id;
    }
    inputEmail () {
        return email;
    }

    role () {
        return 'Employee'
    }
};



















module.exports = Employee; 